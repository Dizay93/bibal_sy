  public static java.sql.Date convertUtilDateToSqlDate(java.util.Date date){
   
      
      
      
      if(date != null) {
        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        return sqlDate;
    }
    else
    {
        JOptionPane.showMessageDialog(null,"Enter Une Date D'Emprunt","Erreur",JOptionPane.ERROR_MESSAGE);
        JOptionPane.showMessageDialog(null,"Enter Une Date De Retour Prevue","Erreur",JOptionPane.ERROR_MESSAGE);

    }
    return null;
}
   
