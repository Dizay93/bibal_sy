    private void TableEmpruntMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableEmpruntMouseClicked
        // TODO add your handling code here:
         try {
            int row=TableEmprunt.getSelectedRow();
            String Table_click=(TableEmprunt.getModel().getValueAt(row, 0).toString());
            
            
            pc.FindForJtable(Table_click);
            if(DbInteraction.rs.next()){
                
                
                String add1=DbInteraction.rs.getString("ExemplaireID");
                exmpid.setText(add1);
                String add2=DbInteraction.rs.getString("UsagerID");
                usID.setText(add2);
                Date dateemp= DbInteraction.rs.getDate("Date_Emprunt");
                dateempr.setDate(dateemp);
                Date datepr= DbInteraction.rs.getDate("Date_Retour_Prevue");
                dateempprev.setDate(datepr);
                Date dateret= DbInteraction.rs.getDate("Date_Retour_Effectif");
                dateempprev.setDate(dateret);
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        
    }//GEN-LAST:event_TableEmpruntMouseClicked
